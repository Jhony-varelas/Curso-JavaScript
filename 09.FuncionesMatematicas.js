// FUNCIONES MATEMATICAS

// VALOR ABSOLUTO
console.log(Math.abs(-45))

//POTENCIA  
//POW (base, exponente  2 elevado a la 3)
console.log(Math.pow(2,3))

//RAIZ CUADRADA
console.log(Math.sqrt(4))

//REDONDEAR UNA CIFRA AL MAS CERCANO
//Round(5.788767)
console.log(Math.round(5.788767))

//NUMERO ALEATORIO RANDOM
// nos da valores aleatorios entre el 0 y 10 pero con numero decimal.
console.log(Math.random()*10)

// nos da el aleatorio sin decimales desde 0 hasta el numero que digamos
console.log(Math.round(Math.random()*10))

// aca no pasa de 10 ni menos de 5
console.log(Math.round(Math.random()*(10-5)+5))


//POSITIVO O NEGATIVO
//si da 1 es positivo y si no negativo -1
console.log(Math.sign(-4))

//REDONDEA AL NUMERO ENTERO SUPERIOR
console.log(Math.ceil(4.7))

//REDONDEA AL ENTERO INFERIOR
console.log(Math.floor(4.7))





